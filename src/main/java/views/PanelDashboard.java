   private void initializeTableModel() {
        tableModel = new DefaultTableModel(
            new Object[][]{}, 
            new String[]{"No", "Nama", "Jabatan", "Departemen", "Status"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblKaryawan.setModel(tableModel);
        
        // Set column widths
        tblKaryawan.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblKaryawan.getColumnModel().getColumn(1).setPreferredWidth(200);
        tblKaryawan.getColumnModel().getColumn(2).setPreferredWidth(150);
        tblKaryawan.getColumnModel().getColumn(3).setPreferredWidth(150);
        tblKaryawan.getColumnModel().getColumn(4).setPreferredWidth(100);
    }

    private void loadDashboardData() {
        try {
            DashboardController controller = new DashboardController();
            
            // Load summary data
            int[] stats = controller.getDashboardStats();
            lblTotalKaryawan.setText(String.valueOf(stats[0]));
            lblKaryawanMasuk.setText(String.valueOf(stats[1]));
            lblTotalCuti.setText(String.valueOf(stats[2]));
            
            // Load table data
            List<Karyawan> karyawanList = controller.getKaryawanList();
            tableModel.setRowCount(0); // Now safe to call
            
            int counter = 1;
            for (Karyawan karyawan : karyawanList) {
                tableModel.addRow(new Object[]{
                    counter++,
                    karyawan.getNama(),
                    karyawan.getJabatan(),
                    karyawan.getDepartemen(),
                    karyawan.getStatus()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error loading data: " + e.getMessage(),
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    
    
